//this is called variable declaration
let fname;

//this is called initialization and we can assign any data to the varaible using "Assignment Operator".
fname = "Naimal"  

alert(fname)

//declare multiple variable in one line
let userName = "Salahuddin",  age = 50 , isMarried = true

alert(userName)
alert(age)
alert(isMarried)

//We can also declare two variables and copy data from one into the other.
let greet = "Hello World"

let message;

message = greet

alert(message)


//Variable definition with "var", "let" ,"const"
var personName = "Asra"
let UserID = 23245
const PI = 3.14

alert(personName)
alert(UserID)
alert(PI)



